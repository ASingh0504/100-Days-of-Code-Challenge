import java.util.*;
public class Squire {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		System.out.println("Enter the side of Squire");
		double n = sc.nextDouble();
		double ar = n*n;
		double pr = 4*n;
		System.out.println("Area of Squire is "+ar);
		System.out.println("Perimeter of Squire is "+pr);

	}

}
